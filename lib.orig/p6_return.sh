# This works like MixIns in Ruby

######################################################################
#<
#
# Function:
#	p6_return_obj_ref()
#
#>
######################################################################
p6_return_obj_ref() {
    local obj="$1"

    p6_return__ "$obj"
}

######################################################################
#<
#
# Function:
#	p6_return_item_ref(item)
#
#  Args:
#	item -
#
#>
######################################################################
p6_return_item_ref() {
    local item="$1"

    p6_return__ "$item"
}
